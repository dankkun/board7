LOOP

INSERT INTO BOARD (
    BNO,
    MENU_ID,
    TITLE,
    CONTENT,
    WRITER,
    REGDATE,
    HIT
) VALUES (
    I,
    'MENU01',
    DATA1 || I,
    '내용',
    'SKY',
    SYSDATE,
    0
);

END LOOP;
COMMIT;
END;


