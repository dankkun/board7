SET TIMING ON
SELECT *
FROM
(SELECT
    ROW_NUMBER() OVER (ORDER BY BNO DESC) RN,
    BNO,
    MENU_ID,
    TITLE,
    CONTENT,
    WRITER,
    REGDATE,
    HIT
FROM
    BOARD) T
WHERE RN BETWEEN 1 AND 10;


3. OFFSET .. FETCH ..

SET TIMING ON
SELECT
    BNO,
    MENU_ID,
    TITLE,
    CONTENT,
    WRITER,
    TO_CHAR(REGDATE, 'YYYY-MM-DD') REGDATE,
    HIT
FROM BOARD
ORDER BY BNO DESC
OFFSET 10 ROWS FETCH NEXT 10 ROW ONLY;
